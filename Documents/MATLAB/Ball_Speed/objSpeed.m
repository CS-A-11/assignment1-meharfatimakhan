function play_Callback(hObject, eventdata, handles)
global fp;
global videoSrc;
videoSrc = VideoReader(fp);
axes(handles.axes1);
implay(fp);
global displayVideo; 
readVideo = vision.VideoFileReader(fp); 
frameRate = videoSrc.FrameRate; 
videoPlayer = vision.VideoPlayer; 
ax = handles.axes1;
i=0;
elapsedTime=0;
set(handles.edit3,'String',num2str(frameRate));
set(handles.edit13,'String',num2str(videoSrc.Duration));
while ~isDone(readVideo) 
    videoFrame = step(readVideo);
    set(handles.edit1,'String',num2str(i));
    elapsedTime=elapsedTime+(1/frameRate);
    set(handles.edit4,'String',num2str(elapsedTime));
%     %disp(handles.value);
%     handles.value=get(handles.checkbox1,'Value');
%     ifPause=handles.value;
    i=i+1;
    image(videoFrame, 'Parent', ax); 
    set(ax,'Visible', 'on');
    pause(1/frameRate); 
%    if (ifPause == 0)
%         pause(ceil(1/frameRate)); 
%    end
   
    if displayVideo ~=1 
        if displayVideo == 1 
            break 
        end
    end
end
release(videoPlayer);

